DECLARE @FILIAL VARCHAR(02) = '01' 
DECLARE @CONTA  VARCHAR(20) = '1222003' 
DECLARE @MES    VARCHAR(02) = '01' 
DECLARE @ANO    VARCHAR(04) = '2016' 

SELECT @MES MES,
ADICAO = (
    SELECT COALESCE( SUM( SN5.N5_VALOR1 ), 0 )
	FROM SN5990 SN5
	WHERE SN5.D_E_L_E_T_ = ' '
	AND SN5.N5_FILIAL = @FILIAL
	AND SN5.N5_CONTA = @CONTA
	AND SUBSTRING(SN5.N5_DATA, 1, 6) = @ANO + @MES
	AND SN5.N5_TIPO IN( '1', '4' ) ),   
ENTRADAS = (
    SELECT COALESCE( SUM( SN5.N5_VALOR1 ), 0 )
	FROM SN5990 SN5
	WHERE SN5.D_E_L_E_T_ = ' '
	AND SN5.N5_FILIAL = @FILIAL
	AND SN5.N5_CONTA = @CONTA
	AND SUBSTRING(SN5.N5_DATA, 1, 6) = @ANO + @MES
	AND SN5.N5_TIPO IN( '9', 'Q', 'T' ) ),
SAIDAS = (
    SELECT COALESCE( SUM( SN5.N5_VALOR1 ), 0 )
	FROM SN5990 SN5
	WHERE SN5.D_E_L_E_T_ = ' '
	AND SN5.N5_FILIAL = @FILIAL
	AND SN5.N5_CONTA = @CONTA
	AND SUBSTRING(SN5.N5_DATA, 1, 6) = @ANO + @MES
	AND SN5.N5_TIPO IN( '8', 'R', 'S' ) ),
BAIXAS = (
    SELECT COALESCE( SUM( SN5.N5_VALOR1 ), 0 )
	FROM SN5990 SN5
	WHERE SN5.D_E_L_E_T_ = ' '
	AND SN5.N5_FILIAL = @FILIAL
	AND SN5.N5_CONTA = @CONTA
	AND SUBSTRING(SN5.N5_DATA, 1, 6) = @ANO + @MES
   	AND SN5.N5_TIPO IN( '5' ) )